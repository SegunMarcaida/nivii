@pytest.mark.integration
async def test_forward_vs_return_revenue_per_product(ask):
    """Per-product breakdown of forward revenue vs return revenue via CASE WHEN."""
    body = await ask("Show me the forward sales revenue and return revenue for the top 10 products.")
    results = body["results"]
    assert len(results) >= 1
    for row in results:
        assert str_col(row, "product", "name")
        # If both forward and net columns exist, check net <= forward invariant
        fwd_key = find_col(row, "forward")
        net_key = find_col(row, "net")
        if fwd_key and net_key:
            forward = float(row[fwd_key])
            net = float(row[net_key])
            assert net <= forward + 0.01, f"net ({net}) > forward ({forward})"


