@pytest.mark.integration
async def test_revenue_on_earliest_date(ask):
    """Revenue on the earliest date uses MIN(sale_date) subquery and proper JOIN."""
    body = await ask("How much did we sell on the earliest date in the database?")
    results = body["results"]
    assert len(results) >= 1
    total = numeric_col(results[0], "revenue", "total", "sum", "amount")
    assert _within_tolerance(total, GROUND_TRUTH_EARLIEST_DATE_REVENUE)


