"""
Integration tests — challenging and super-challenging queries through the full pipeline.
No mocks: real Ollama inference + real DB execution.

Assertions compare model results against ground truth values queried directly from the DB.
"""
import pytest

from tests.integration.conftest import find_col, numeric_col, str_col

TOLERANCE = 0.05  # 5% tolerance for revenue comparisons


def _within_tolerance(actual: float, expected: float, tol: float = TOLERANCE) -> bool:
    if expected == 0:
        return actual == 0
    return abs(actual - expected) / abs(expected) <= tol


# ─────────────────────────────────────────────────────────────────────────────
# Ground truth constants
# ─────────────────────────────────────────────────────────────────────────────
GROUND_TRUTH_EARLIEST_DATE_REVENUE = 4_467_600
GROUND_TRUTH_BEST_GROSS_MONTH = (2024, 10, 109_339_350)
GROUND_TRUTH_TOP_FRIDAY_WAITER = "Vendedor 103"
GROUND_TRUTH_TOP_AVG_ITEMS_WAITER = ("Vendedor 116", 3.01)
GROUND_TRUTH_TOP_COPURCHASED = (4, 9, 416)  # product_a, product_b, count
GROUND_TRUTH_TOTAL_REVENUE = 211_994_375


