@pytest.mark.integration
async def test_product_revenue_rank_window_function(ask):
    """RANK() window function assigns ordinal rank to each product by revenue."""
    body = await ask("Rank all products by their total revenue using a window function.")
    results = body["results"]
    assert len(results) >= 1
    rank_key = find_col(results[0], "rank")
    if rank_key is not None:
        assert int(results[0][rank_key]) == 1, "First row must have rank 1"
        ranks = [int(r[rank_key]) for r in results]
        assert ranks == sorted(ranks), "Ranks must be non-decreasing"


@pytest.mark.integration
async def test_product_revenue_percentage_share(ask):
    """Each product's revenue as a % of total using SUM() OVER () window aggregate."""
    body = await ask("What percentage of total revenue does each product contribute?")
    results = body["results"]
    assert len(results) >= 1
    pct_key = find_col(results[0], "pct", "percent", "share")
    if pct_key is not None:
        pcts = [float(r[pct_key]) for r in results]
        for pct in pcts:
            assert 0 <= pct <= 100, f"Percentage {pct} out of 0-100 range"
        total_pct = sum(pcts)
        assert 99.0 <= total_pct <= 101.0, f"Percentages sum to {total_pct}, expected ~100"


