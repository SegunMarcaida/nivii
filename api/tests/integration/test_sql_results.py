"""
Integration tests — 12 NL questions through the full pipeline (real Ollama + real DB).
No mocks: each question goes through prompt construction → Ollama inference →
SQL parsing/cleaning → EXPLAIN validation → DB execution → answer generation.

Assertions compare model results against ground truth values queried directly from the DB.
"""
import pytest

from tests.integration.conftest import find_col, numeric_col, str_col

# ─────────────────────────────────────────────────────────────────────────────
# Ground truth constants (queried directly from the live DB)
# ─────────────────────────────────────────────────────────────────────────────
GROUND_TRUTH_TOP5_PRODUCTS = [
    ("Alfajor Sin Azucar Suelto", 4566),
    ("Alf. 150 aniv. Suelto", 3795),
    ("Alfajor choc x un", 1909),
    ("Alfajor mixto caja x12un", 1700),
    ("Alfajor choc blanco nuez x un", 1660),
]
GROUND_TRUTH_FRIDAY_PRODUCT = "Alfajor Sin Azucar Suelto"
GROUND_TRUTH_FRIDAY_PRODUCT_QTY = 850
GROUND_TRUTH_TOP_WAITER_REVENUE = ("Vendedor 103", 53_365_500)
GROUND_TRUTH_GIVEAWAY_TICKETS = 427
GROUND_TRUTH_HIGHEST_AVG_PRICE_PRODUCT = "Caja MIX Locales"
GROUND_TRUTH_HIGHEST_AVG_PRICE = 55_000
GROUND_TRUTH_ITEMS_PER_MONTH = [(2024, 9, 4712), (2024, 10, 16748.5), (2024, 11, 9950)]
GROUND_TRUTH_TOP_WAITER_TRANSACTIONS = ("Vendedor 103", 3124)
GROUND_TRUTH_TOTAL_REVENUE = 211_994_375
GROUND_TRUTH_PRODUCT_COUNT = 68
GROUND_TRUTH_TICKET_COUNT = 11_771
GROUND_TRUTH_TOP_TICKET = ("FCA 0003-000000111", 605_000)
TOLERANCE = 0.05  # 5% tolerance for revenue comparisons


def _within_tolerance(actual: float, expected: float, tol: float = TOLERANCE) -> bool:
    """Check if actual is within ±tol% of expected."""
    if expected == 0:
        return actual == 0
    return abs(actual - expected) / abs(expected) <= tol


# ─────────────────────────────────────────────────────────────────────────────
# Test 1 — Top 5 products by quantity sold
# ─────────────────────────────────────────────────────────────────────────────

@pytest.mark.integration
async def test_top5_products_by_quantity(ask):
    body = await ask("What are the top 5 best-selling products by total quantity sold?")
    results = body["results"]
    assert 1 <= len(results) <= 10
    # The #1 product must be "Alfajor Sin Azucar Suelto"
    top_name = str_col(results[0], "product", "name")
    assert GROUND_TRUTH_TOP5_PRODUCTS[0][0] in top_name
    # Top quantity must be close to ground truth
    top_qty = numeric_col(results[0], "quantity", "qty", "total")
    assert _within_tolerance(top_qty, GROUND_TRUTH_TOP5_PRODUCTS[0][1])
    # Sorted descending
    qtys = [numeric_col(r, "quantity", "qty", "total") for r in results]
    assert qtys == sorted(qtys, reverse=True)


